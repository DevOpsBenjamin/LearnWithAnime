fn main() -> Result<()> {
    let project_root = Path::new(env!("CARGO_MANIFEST_DIR"));
    let output_dir = project_root
        .parent()
        .unwrap()
        .join("backend")
        .join("data")
        .join("radicals");
    fs::create_dir_all(&output_dir)?;

    let path = output_dir.join("kangxi.json");
    let mut file = fs::File::create(&path).context("Cannot create kangxi.json")?;

    writeln!(file, "[")?;
    for (i, (number, char, meaning, strokes)) in RADICALS.iter().enumerate() {
        let comma = if i < RADICALS.len() - 1 { "," } else { "" };
        let entry = serde_json::json!({
            "number": number,
            "char": char,
            "meaning": meaning,
            "stroke_count": strokes,
        });
        writeln!(file, "  {}{}", entry, comma)?;
    }
    writeln!(file, "]")?;

    println!("✓ {} radicaux KangXi exportés → {}", RADICALS.len(), path.display());
    Ok(())
}
